def addition (a,b):
    add = a+b
    return add
def substraction (a,b):
    sub = a-b
    return sub
def multiplication (a,b):
    mul = a*b
    return mul
def division (a,b):
    div = a/b
    return div

print (addition(5,10))

print (substraction(5,10))

print (multiplication(5,10))

print (division(5,10))


